    private void solBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solBActionPerformed
        if(p != null){
            double dinero = 0;
            try {
               dinero = Double.parseDouble(vlrJTF.getText().trim());
            } catch (Exception e) {
                JOptionPane.showMessageDialog
                (this, "Se debe ingresar un valor aceptable, sin espacios ni puntos entre numeros"
                 , "Error",JOptionPane.ERROR_MESSAGE);
                dinero = 0;
            }
            if(dinero > 0){
                if(dinero <= 10000000){
                    if(dinero <= coop.getDineroParaPrestar()){
                          int cuotas = Integer.parseInt((String) numCCB.getSelectedItem());
                          Controlador.getInstance().Solicitud(p, dinero, cuotas);
                    }else{
                        JOptionPane.showMessageDialog
                        (this, "El Monto no debe sobrepasar el dinero disponible"
                 , "Error",JOptionPane.ERROR_MESSAGE);
                    }
                }else{    
                JOptionPane.showMessageDialog
                (this, "El Monto Sobrepasa el maximo dinero permitido en un prestamo"
                 , "Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            
        }
    }//GEN-LAST:event_solBActionPerformed

