    private void cliConsultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cliConsultActionPerformed
        if(!ccTF.getText().trim().equals("")){
              int cc = 0;
        try {
            cc = Integer.parseInt(ccTF.getText().trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "EL valor Cedula no es correcto\n"
                    + "Recuerda que solo se admiten valores numericos sin espacios entre ellos", "Error", JOptionPane.ERROR_MESSAGE);
            cc = 0;
        }
        if(cc != 0){
             p = Controlador.getInstance().consultaPorCC(cc);
            if(p != null){
                nameTF.setText(p.getNombre());
            }
            else
                JOptionPane.showMessageDialog(this, "No se encontro el cliente asegurese de que este registrado o que escribio bien la cedula", "error", JOptionPane.ERROR_MESSAGE);
        }
            
        }
    }//GEN-LAST:event_cliConsultActionPerformed

