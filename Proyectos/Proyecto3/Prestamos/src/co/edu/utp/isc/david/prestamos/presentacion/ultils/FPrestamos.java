    private void SimularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimularActionPerformed
       int numcuotas =  Integer.parseInt((String)numCCB.getSelectedItem());
       double vlr = 0;
        try {
          vlr = Double.parseDouble(vlrJTF.getText());
        } catch (Exception e) {
            vlr = 0;
        }
       
       double vlrinteres = 0.2;
       double cuota = ((vlr * vlrinteres) + vlr) / numcuotas;
       JOptionPane.showMessageDialog(this,"El valor de las  coutas es "
       + String.format("%.3f", cuota) ,"Simulador Credito",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_SimularActionPerformed

